    std::optional<Document> LoadJsonFromFile(const char* file_path) {
        Document doc;
        std::ifstream file(file_path);
        if (file.is_open()) {
            std::string content((std::istreambuf_iterator<char>(file)),
                                (std::istreambuf_iterator<char>()));
            if (doc.Parse(content.c_str()).HasParseError()) {
                if (content.size() > 2) {
                    content[doc.GetErrorOffset() - 2] = '-';
                    content[doc.GetErrorOffset() - 1] = '>';
                }
                ERROR("[%s] %s #%d\nFile: %s %s %zu %s", __FUNCTION__, __FILE__, __LINE__,
                      file_path, GetParseError_En(doc.GetParseError()), doc.GetErrorOffset(), content.c_str());
                return std::nullopt;
            }
        }
        else {
            ERROR("[%s] %s #%d\nCould not read file: %s. %s", __FUNCTION__, __FILE__, __LINE__, file_path, strerror(errno));
            return std::nullopt;
        }
        return doc;
    }

    jobject GetApplication(JNIEnv* env) {
        jclass activity_thread_clz = env->FindClass("android/app/ActivityThread");
        if (activity_thread_clz != nullptr) {
            jmethodID currentApplicationId = env->GetStaticMethodID(
                activity_thread_clz,
                "currentApplication",
                "()Landroid/app/Application;");
            if (currentApplicationId != nullptr) {
                return env->CallStaticObjectMethod(activity_thread_clz, currentApplicationId);
            }
            else {
                ERROR("Cannot find method: currentApplication() in ActivityThread.");
            }
        }
        else {
            ERROR("Cannot find class: android.app.ActivityThread");
        }
        return nullptr;
    }

    std::optional<jobject> GetApplicationInfo(JNIEnv* env) {
        jobject application = GetApplication(env);
        if (application == nullptr) {
            return std::nullopt;
        }
        jclass application_clazz = env->GetObjectClass(application);
        if (application_clazz == nullptr) {
            return std::nullopt;
        }
        jmethodID get_application_info = env->GetMethodID(
            application_clazz,
            "getApplicationInfo",
            "()Landroid/content/pm/ApplicationInfo;");
        if (get_application_info) {
            return env->CallObjectMethod(application, get_application_info);
        }
        else {
            return std::nullopt;
        }
    }

    std::optional<std::string> GetLibraryPath(JNIEnv* env, jobject application_info) {
        if (!application_info) {
            return std::nullopt;
        }
        jfieldID native_library_dir_id = env->GetFieldID(
            env->GetObjectClass(application_info),
            "nativeLibraryDir",
            "Ljava/lang/String;");

        if (native_library_dir_id) {
            jstring native_library_dir_jstring = (jstring)env->GetObjectField(application_info, native_library_dir_id);
            const char* path = env->GetStringUTFChars(native_library_dir_jstring, 0);
            std::string package_name(path);
            env->ReleaseStringUTFChars(native_library_dir_jstring, path);
            return package_name;
        }
        else {
            return std::nullopt;
        }
    }

    std::optional<JavaVM*> GetVM() {
        // copy from https://github.com/frida/frida-core/blob/main/lib/agent/agent.vala

        void* art = xdl_open("libart.so", 0);
        if (!art) {
            return std::nullopt;
        }

        using JNIGetCreatedJavaVMs_t = int (*)(JavaVM** vmBuf, jsize bufLen, jsize* nVMs);
        static JNIGetCreatedJavaVMs_t JNIGetCreatedJavaVMsFunc = (JNIGetCreatedJavaVMs_t)xdl_sym(art, "JNI_GetCreatedJavaVMs", nullptr);

        if (!JNIGetCreatedJavaVMsFunc) {
            return std::nullopt;
        }

        jsize numVMs;
        JavaVM* vms = nullptr;
        if (JNIGetCreatedJavaVMsFunc(&vms, 1, &numVMs) != 0) {
            return std::nullopt;
        }
        return vms;
    }

